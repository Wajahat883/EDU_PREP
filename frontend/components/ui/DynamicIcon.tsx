export const DynamicIcon: React.FC<IconProps> = ({
  name,
  className = "",
  size = 24,
}) => {
  const iconPath = iconPaths[name];

  if (!iconPath) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }

  // Social icons use fill instead of stroke
  const isSocialIcon = [
    "twitter",
    "linkedin",
    "instagram",
    "youtube",
    "github",
  ].includes(name);

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={isSocialIcon ? "currentColor" : "none"}
      stroke={isSocialIcon ? "none" : "currentColor"}
      className={className}
    >
      {iconPath}
    </svg>
  );
};

